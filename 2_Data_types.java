class DataTypes {
    public static void main(String[] args) {
        System.out.println("Data Types");

        byte clas = 12; // 8 bit (-128 : 128)
        short age = 36; // 16 bit (-32768 : 32768)
        int cur_date = 2024; // 32 bit
        long cur_popu = 7888400000l; // 64 bit
        float height = 201.2f;
        double height1 = 200000.45d;
        boolean t = true;

        System.out.println(clas);
        System.out.println(age);
        System.out.println(cur_date);
        System.out.println(cur_popu);
        System.out.println(height);
        System.out.println(height1);
        System.out.println(t);

    }

}
